namespace NjulfFramework.Input.Enums;

/// <summary>
///     Represents the type of input binding.
/// </summary>
public enum InputBindingType
{
    /// <summary>
    ///     Keyboard key binding.
    /// </summary>
    Key,
    
    /// <summary>
    ///     Mouse button binding.
    /// </summary>
    MouseButton,
    
    /// <summary>
    ///     Mouse X axis delta binding.
    /// </summary>
    MouseXAxis,
    
    /// <summary>
    ///     Mouse Y axis delta binding.
    /// </summary>
    MouseYAxis
}
